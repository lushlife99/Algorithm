WITH RECURSIVE GENERATION_CTE AS (
    SELECT 
        ID,
        PARENT_ID,
        1 AS GENERATION
    FROM ECOLI_DATA
    WHERE PARENT_ID IS NULL
    UNION ALL
    SELECT 
        E.ID,
        E.PARENT_ID,
        G.GENERATION + 1
    FROM ECOLI_DATA E
    JOIN GENERATION_CTE G
      ON E.PARENT_ID = G.ID
)
SELECT 
    COUNT(*) AS COUNT,
    GENERATION
FROM GENERATION_CTE G
WHERE G.ID NOT IN (
    SELECT DISTINCT PARENT_ID
    FROM ECOLI_DATA
    WHERE PARENT_ID IS NOT NULL
)
GROUP BY GENERATION
ORDER BY GENERATION;
